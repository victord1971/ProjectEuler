# Таблиця значень A(m,n) до m=5, n=5
# Індекси таблиці відповідають значенням m та n
a_table = [
    [0, 1, 1, 4, 7, 19],
    [1, 2, 5, 11, 26, 59],
    [3, 7, 16, 37, 85, 196],
    [10, 23, 53, 122, 281, 647],
    [33, 76, 175, 403, 928, 2137],
    [109, 251, 578, 1331, 3065, 7058]
]

# Послідовність Фібоначчі: F[0]=0, F[1]=1, F[2]=1, F[3]=2, F[4]=3, F[5]=5, F[6]=8...
# Нам потрібні F_i для i від 2 до 5
fib_sequence = [0, 1, 1, 2, 3, 5, 8] # Достатньо до F_6 для S(5)

# Розрахунок S(5) = sum[i=2..5] sum[j=2..5] A(F_i, F_j)
s_5 = 0
for i in range(2, 6): # i від 2 до 5 включно
    for j in range(2, 6): # j від 2 до 5 включно
        # Отримуємо значення m та n з послідовності Фібоначчі
        m = fib_sequence[i]
        n = fib_sequence[j]

        # Отримуємо значення A(m,n) з таблиці
        # Перевіряємо, чи m та n знаходяться в межах таблиці
        if m < len(a_table) and n < len(a_table[0]):
            s_5 += a_table[m][n]
        else:
            print(f"Помилка: Значення A({m},{n}) відсутнє в таблиці.")
            s_5 = None # Встановлюємо None, щоб показати помилку
            break # Виходимо з внутрішнього циклу
    if s_5 is None:
        break # Виходимо із зовнішнього циклу, якщо була помилка

# Виведення результату
if s_5 is not None:
    print(f"Розраховане S(5) = {s_5}")

